	class Sms{
		/*
			发送短息
		*/
		public function send_sms(){
			if(D('sms','space')->sms_send($_POST['f_uid'],$_SESSION['user_info']['id'],$_POST['content'])){
				$this->success('短信发送成功!',1);
			}else{
				$this->error('短信发送失败',2);
			}
		}
	}
